#include "libft.h"

void	*ft_memmove(void *dest, const void *src, size_t n)
{
	void	*dcpy;

	dcpy = dest;
	if (!dest && !src)
		return (dest);
	if (dest == src)
		return (dest);
	if (dest > src)
	{
		while (n--)
			((char *)dest)[n] = ((char *)src)[n];
	}
	else
	{
		while (n--)
			*(char *)dest++ = *(char *)src++;
	}
	return (dcpy);
}
